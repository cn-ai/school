package com.accp.exam.pojo;

public class Detail {
private int id;
private int invid;
private String content;
private String author;
private String createdatel;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getInvid() {
	return invid;
}
public void setInvid(int invid) {
	this.invid = invid;
}
public String getContent() {
	return content;
}
public void setContent(String content) {
	this.content = content;
}
public String getAuthor() {
	return author;
}
public void setAuthor(String author) {
	this.author = author;
}
public String getCreatedatel() {
	return createdatel;
}
public void setCreatedatel(String createdatel) {
	this.createdatel = createdatel;
}


}
